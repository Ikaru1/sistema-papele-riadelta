    private void limpiarTabla(JTable tabla) {
        DefaultTableModel model = (DefaultTableModel) tabla.getModel();
        for (int i = model.getRowCount(); i != 0; i--) {
            model.removeRow(i - 1);
        }
        tabla.setModel(model);
    }

    private void listarClientes(JTable tabla) {
        limpiarTabla(tabla);
        DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
        LinkedList lista = clienteDAO.listar();
        if (lista != null) {
            for (int i = 0; i < lista.size(); i++) {
                Cliente clienteAuxiliar = (Cliente) lista.get(i);
                Object[] datos = {clienteAuxiliar.getId(),
                    clienteAuxiliar.getNombre(),
                    clienteAuxiliar.getNit(),
                    clienteAuxiliar.getTelefono()};
                modelo.addRow(datos);
            }
        }
        tabla.setModel(modelo);
    }
