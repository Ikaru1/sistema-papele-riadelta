        registroCliente.setVisible(true);
        clienteBqd = registroCliente.getClienteBqd();
        if (clienteBqd != null) {
            limpiarTabla(tblListaCliente);
            DefaultTableModel modelo = (DefaultTableModel) tblListaCliente.getModel();
            Cliente clienteAuxiliar = (Cliente) clienteBqd;
            Object[] datos = {clienteAuxiliar.getId(),
                clienteAuxiliar.getNombre(),
                clienteAuxiliar.getNit(),
                clienteAuxiliar.getTelefono()};
            modelo.addRow(datos);
            tblListaCliente.setModel(modelo);
        } else {
            JOptionPane.showMessageDialog(null, "EL CLIENTE NO EXISTE");
        }
