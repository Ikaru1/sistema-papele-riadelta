    private boolean esValidoLaInformacionDeEntrada(String nombre, String precioReferencia, String marca) {
        boolean verificado = false;
        
        if (!nombre.isEmpty()) {
            if (!marca.isEmpty()) {
                if (!precioReferencia.isEmpty()) {
                    if (esNumeroDecimal(precioReferencia)) {
                        verificado = true;
                    }
                } else {
                    verificado = true;
                }
            } else {
                JOptionPane.showMessageDialog(null, "la Marca no puede ser vacia");                
            }
        } else {
            JOptionPane.showMessageDialog(null, "El nombre del producto no puede ser vacio");
        }
        
        
        return verificado;
    }

    /**
     * este metodo verifica si un numero es decimal
     *
     * @param numero
     * @return boolean
     */
    private boolean esNumeroDecimal(String numero) {
        boolean verificado = true;
        try {
            Float.parseFloat(numero);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio referencia es invalido");
            verificado = false;
        }
        return verificado;
    }

    /**
     * este metodo verifica si un numero es decimal
     *
     * @param numero
     * @return boolean
     */
    private boolean esNumero(String numero) {
        boolean verificado = true;
        try {
            Integer.valueOf(numero);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio referencia es invalido");
            verificado = false;
        }
        return verificado;
    }

    /**
     *
     */
    private boolean agregarProducto() {
        boolean resultado = false;
        
        String nombre = txtNombre.getText().trim();
        String precioReferencia = txtPrecioReferencia.getText().trim();
        String observacion = txtObservacion.getText().trim();
        float precioReferenciaConvertido = 0;
        boolean esValido = esValidoLaInformacionDeEntrada(nombre, precioReferencia, observacion);
        boolean esUnico = nProducto.esProductoNuevo(nombre, observacion);
        if (esValido && esUnico) {
            
            if (!precioReferencia.isEmpty()) {
                precioReferenciaConvertido = Float.parseFloat(precioReferencia);
            }
            
            nProducto.agregar(nombre, 0, observacion, precioReferenciaConvertido);
            JOptionPane.showMessageDialog(null, "El producto se ha agregado correctamente");
            resultado = true;
        }
        
        return resultado;
    }
    
    private void mostrarDatos(String[] datos) {
        txtCodigo.setText(datos[0]);
        txtNombre.setText(datos[2]);
        txtPrecioReferencia.setText(datos[3]);
        txtObservacion.setText(datos[4]);
    }
    
    private boolean modificarProducto() {
        boolean resultado = false;
        
        String nombre = txtNombre.getText().trim();
        String precioReferencia = txtPrecioReferencia.getText().trim();
        String observacion = txtObservacion.getText().trim();
        
        boolean esValido = esValidoLaInformacionDeEntrada(nombre, precioReferencia, observacion);
        
        if (esValido) {
            float precioReferenciaConvertido = Float.parseFloat(precioReferencia);
            int codigo = Integer.valueOf(txtCodigo.getText());
            int stock = nProducto.stockDisponible(codigo);
            nProducto.modificar(codigo, nombre, stock, observacion, precioReferenciaConvertido);
            JOptionPane.showMessageDialog(null, "El producto se ha modificado correctamente");
            resultado = true;
        }
        
        return resultado;
        
    }
